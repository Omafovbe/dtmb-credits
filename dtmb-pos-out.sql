BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "users" (
	"id"	INTEGER NOT NULL,
	"username"	VARCHAR NOT NULL,
	"fullname"	VARCHAR(50) NOT NULL,
	"hashed_password"	VARCHAR NOT NULL,
	"role"	VARCHAR(5) NOT NULL,
	"created_at"	DATETIME NOT NULL DEFAULT (CURRENT_TIMESTAMP),
	UNIQUE("username"),
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "pos_agents" (
	"id"	INTEGER NOT NULL,
	"terminal_id"	VARCHAR NOT NULL,
	"account_number"	VARCHAR(10) NOT NULL,
	"agent_name"	VARCHAR NOT NULL,
	UNIQUE("terminal_id"),
	UNIQUE("account_number"),
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "transactions" (
	"id"	INTEGER NOT NULL,
	"retrieval_ref"	VARCHAR NOT NULL,
	"amount"	FLOAT NOT NULL,
	"reference"	VARCHAR NOT NULL,
	"status"	VARCHAR(12) NOT NULL,
	"pos_id"	INTEGER NOT NULL,
	"created_at"	DATETIME NOT NULL DEFAULT (CURRENT_TIMESTAMP),
	FOREIGN KEY("pos_id") REFERENCES "pos_agents"("id"),
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "cus_credits" (
	"id"	INTEGER NOT NULL,
	"retrieval_ref"	VARCHAR NOT NULL,
	"account_number"	VARCHAR NOT NULL,
	"narration"	VARCHAR NOT NULL,
	"amount"	FLOAT NOT NULL,
	"reference"	VARCHAR NOT NULL DEFAULT A2023,
	"staff"	VARCHAR NOT NULL,
	"created_at"	DATETIME NOT NULL DEFAULT (CURRENT_TIMESTAMP),
	PRIMARY KEY("id")
);
